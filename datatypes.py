x=5.5
print(type(x))
print(x)

# 3. complex
x=5+3j
print(type(x))
print(x)

# 4. str
x="Hello"
print(type(x))
print(x)

# 5. bool
x=True
print(type(x))
print(x)

# 6. list
x=[1,2,3,4,5]
print(type(x))
print(x)

# 7. tuple
x=(1,2,3,4,5)
print(type(x))
print(x)

# 8. set
x={1,2,3,4,5}
print(type(x))
print(x)

# 9. dict
x={1:"one",2:"two",3:"three"}
print(type(x))
print(x)

# 10. frozenset
x=frozenset({1,2,3,4,5})
print(type(x))
print(x)

# 11. bytes
x=b"Hello"
print(type(x))
print(x)

# 12. bytearray
x=bytearray(5)
print(type(x))
print(x)

# 13. memoryview
x=memoryview(bytes(5))
print(type(x))
print(x)

# 14. range
x=range(5)
print(type(x))
print(x)

# 15. None
x=None
print(type(x))
print(x)

# 16. Ellipsis
x=Ellipsis
print(type(x))
print(x)

# 17. NotImplemented
x=NotImplemented
print(type(x))
print(x)

# 18. bool
x=bool(5)
print(type(x))
print(x)


